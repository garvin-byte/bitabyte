bool isAutoGeneratedLabel(const QString& labelText) {
    const QString trimmedLabel = labelText.trimmed();
    return trimmedLabel.isEmpty()
        || trimmedLabel.startsWith(QStringLiteral("BIN "))
        || trimmedLabel.startsWith(QStringLiteral("NIBBLE "))
        || trimmedLabel.startsWith(QStringLiteral("Byte "))
        || trimmedLabel.startsWith(QStringLiteral("Bytes "))
        || trimmedLabel.startsWith(QStringLiteral("Combo "));
}

QString defaultFieldLabel(const features::columns::VisibleByteColumn& firstVisibleColumn,
                          const features::columns::VisibleByteColumn& lastVisibleColumn) {
    const bool startsOnByteBoundary = firstVisibleColumn.absoluteStartBit % 8 == 0;
    const bool endsOnByteBoundary = lastVisibleColumn.absoluteEndBit % 8 == 7;
    const int startByteIndex = firstVisibleColumn.absoluteStartBit / 8;
    const int endByteIndex = lastVisibleColumn.absoluteEndBit / 8;

    if (startsOnByteBoundary && endsOnByteBoundary) {
        if (startByteIndex == endByteIndex) {
            return QStringLiteral("Byte %1").arg(startByteIndex);
        }
        return QStringLiteral("Bytes %1-%2").arg(startByteIndex).arg(endByteIndex);
    }

    return QStringLiteral("Bits %1-%2")
        .arg(firstVisibleColumn.absoluteStartBit)
        .arg(lastVisibleColumn.absoluteEndBit);
}

