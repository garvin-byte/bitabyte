MainWindow::MainWindow(QWidget* parent)
    : QMainWindow(parent),
      byteTableModel_(new models::ByteTableModel(&dataSource_, &frameLayout_, &columnDefinitions_, this)),
      frameGroupTreeModel_(new models::FrameGroupTreeModel(this)),
      fieldInspectorWatcher_(new QFutureWatcher<features::inspector::FieldInspectorAnalysis>(this)) {
    liveBitViewerRefreshTimer_ = new QTimer(this);
    liveBitViewerRefreshTimer_->setSingleShot(true);
    liveBitViewerRefreshTimer_->setInterval(0);
    connect(liveBitViewerRefreshTimer_, &QTimer::timeout, this, &MainWindow::refreshLiveBitViewer);
    fieldInspectorRefreshTimer_ = new QTimer(this);
    fieldInspectorRefreshTimer_->setSingleShot(true);
    fieldInspectorRefreshTimer_->setInterval(0);
    connect(fieldInspectorRefreshTimer_, &QTimer::timeout, this, &MainWindow::refreshFieldInspector);
    connect(fieldInspectorWatcher_, &QFutureWatcher<features::inspector::FieldInspectorAnalysis>::finished, this, [this]() {
        if (fieldInspectorWatcher_ == nullptr) {
            return;
        }

        if (activeFieldInspectorRequestId_ == fieldInspectorRequestId_) {
            if (fieldInspectorPanel_ != nullptr) {
                fieldInspectorPanel_->setAnalysis(fieldInspectorWatcher_->result());
            }
            return;
        }

        refreshFieldInspector();
    });
    setWindowTitle(QStringLiteral("Bitabyte C++ Byte Mode"));
    setAcceptDrops(true);
    buildMenus();
    buildCentralWidget();
    buildColumnDefinitionsDock();
    buildLiveBitViewerDock();
    applyInitialWindowLayout();
    updateLoadedFileState();
}

void MainWindow::openFile() {
    const QString filePath = QFileDialog::getOpenFileName(
        this,
        QStringLiteral("Open Binary File"),
        QString(),
        QStringLiteral("Binary files (*.bin);;All Files (*.*)")
    );
    loadFilePath(filePath);
}

void MainWindow::reloadFile() {
    if (!dataSource_.hasData()) {
        return;
    }

    loadFilePath(dataSource_.sourceFilePath(), false);
}

void MainWindow::exportCsv() {
    if (!dataSource_.hasData()) {
        QMessageBox::information(this, QStringLiteral("Export CSV"), QStringLiteral("Load a file before exporting."));
        return;
    }

    const QString filePath = QFileDialog::getSaveFileName(
        this,
        QStringLiteral("Export Byte Table as CSV"),
        QString(),
        QStringLiteral("CSV files (*.csv);;All Files (*.*)")
    );
    if (filePath.isEmpty()) {
        return;
    }

    QString errorMessage;
    if (!features::exporting::ByteTableCsvExporter::exportToFile(
            dataSource_,
            frameLayout_,
            filePath,
            &errorMessage
        )) {
        QMessageBox::critical(
            this,
            QStringLiteral("Export CSV"),
            QStringLiteral("Failed to write file:\n%1").arg(errorMessage)
        );
        return;
    }

    statusBar()->showMessage(QStringLiteral("Exported CSV"), 3000);
}

