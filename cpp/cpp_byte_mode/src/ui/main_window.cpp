void MainWindow::frameSelection() {
    if (!dataSource_.hasData()) {
        QMessageBox::information(this, QStringLiteral("Frame Selection"), QStringLiteral("Load a file first."));
        return;
    }

    const QSet<int> selectedColumns = selectedVisibleColumns();
    QString patternText;
    QString errorMessage;
    if (!extractSelectionPattern(&patternText, &errorMessage)) {
        QMessageBox::warning(
            this,
            QStringLiteral("Frame Selection"),
            errorMessage.isEmpty() ? QStringLiteral("Select one or more byte cells first.") : errorMessage
        );
        return;
    }

    syncPatternLineEdit_->setText(patternText);
    if (!applySyncFramingPattern(patternText, &errorMessage)) {
        QMessageBox::warning(this, QStringLiteral("Frame Selection"), errorMessage);
        return;
    }

    upsertSyncDefinitionForSelection(selectedColumns);
}

void MainWindow::applySyncFraming() {
    if (!dataSource_.hasData()) {
        QMessageBox::information(this, QStringLiteral("Apply Framing"), QStringLiteral("Load a file first."));
        return;
    }

    QString errorMessage;
    if (!applySyncFramingPattern(syncPatternLineEdit_->text(), &errorMessage)) {
        QMessageBox::warning(this, QStringLiteral("Apply Framing"), errorMessage);
    }
}

void MainWindow::openBitstreamSyncDiscovery() {
    if (!dataSource_.hasData()) {
        QMessageBox::information(
            this,
            QStringLiteral("Bitstream Sync Discovery"),
            QStringLiteral("Load a file before running bitstream sync discovery.")
        );
        return;
    }

    BitstreamSyncDiscoveryDialog dialog(&dataSource_, this);
    if (dialog.exec() != QDialog::DialogCode::Accepted) {
        return;
    }

    const std::optional<features::bitstream_sync_discovery::BitstreamSyncDiscoveryCandidate> selectedCandidate =
        dialog.selectedCandidate();
    if (!selectedCandidate.has_value()) {
        return;
    }

    syncPatternLineEdit_->setText(selectedCandidate->displayPattern);
    allFrameSpans_ = selectedCandidate->frameSpans;
    activeScopedFrameStartBits_.clear();
    frameFilterClauses_.clear();
    resetSplitScopeState();
    if (frameGroupTreeModel_ != nullptr) {
        frameGroupTreeModel_->setActiveScopePath({});
    }
    frameLayout_.setFrames(allFrameSpans_);
    upsertSyncDefinition(0, selectedCandidate->refinedPattern.bitWidth, selectedCandidate->displayFormat);
    syncFramingControlsFromState();
    byteTableModel_->reload();
    resizeTableColumns();
    byteTableView_->clearSelection();
    byteTableView_->setCurrentIndex(QModelIndex());
    updateLoadedFileState();
    updateSelectionStatus();
    refreshLiveBitViewer();
    statusBar()->showMessage(
        QStringLiteral("Applied bitstream sync discovery result: %1 (%2 frames)")
            .arg(selectedCandidate->displayPattern)
            .arg(selectedCandidate->frameSpans.size()),
        5000
    );
}

