void ByteTableModel::rebuildVisibleColumns() {
    visibleColumns_.clear();
    byteColumnStarts_.clear();
    byteColumnCounts_.clear();
    counterVisibleColumnIndices_.clear();
    invalidateConstantVisibleColumnCache(false);
    effectiveColumnDefinitions_.clear();

    if (dataSource_ == nullptr || frameLayout_ == nullptr) {
        return;
    }

    const int widthBytes = displayWidthBytes();
    if (widthBytes <= 0) {
        return;
    }

    if (columnDefinitions_ != nullptr) {
        effectiveColumnDefinitions_ = *columnDefinitions_;
    }
    for (const features::columns::ByteColumnDefinition& detectedFieldDefinition : detectedFieldDefinitions_) {
        effectiveColumnDefinitions_.append(detectedFieldDefinition);
    }

    if (isBitDisplayMode()) {
        const int totalBitCount = frameLayout_->isFramed()
            ? (
                frameLayout_->padFramedBitDisplayToByteBoundary()
                    ? widthBytes * 8
                    : static_cast<int>(qMax<qsizetype>(0, frameLayout_->frameMaxLengthBits()))
            )
            : static_cast<int>(qMax<qsizetype>(0, frameLayout_->rawRowWidthBits()));
        QVector<int> definitionIndexByBit(totalBitCount, -1);
        QVector<QString> splitLabelByBit(totalBitCount);
        QVector<QString> splitColorByBit(totalBitCount);
        QVector<QString> splitDisplayFormatByBit(totalBitCount);

        for (int definitionIndex = 0; definitionIndex < effectiveColumnDefinitions_.size(); ++definitionIndex) {
            const std::optional<QPair<int, int>> bitSpan =
                definitionBitSpan(effectiveColumnDefinitions_.at(definitionIndex));
            if (!bitSpan.has_value()) {
                continue;
            }

            for (int absoluteBit = bitSpan->first; absoluteBit <= bitSpan->second; ++absoluteBit) {
                if (absoluteBit < 0 || absoluteBit >= totalBitCount) {
                    continue;
                }
                if (definitionIndexByBit[absoluteBit] < 0) {
                    definitionIndexByBit[absoluteBit] = definitionIndex;
                }
            }
        }

        for (int byteIndex = 0; byteIndex < widthBytes; ++byteIndex) {
            const QVector<SplitSegment> splitSegments = splitSegmentsForByte(byteIndex);
            for (const SplitSegment& splitSegment : splitSegments) {
                for (int bitIndex = splitSegment.startBit; bitIndex <= splitSegment.endBit; ++bitIndex) {
                    const int absoluteBit = (byteIndex * 8) + bitIndex;
                    if (absoluteBit < 0 || absoluteBit >= totalBitCount || definitionIndexByBit[absoluteBit] >= 0) {
                        continue;
                    }

                    splitLabelByBit[absoluteBit] = splitSegment.label.trimmed();
                    splitColorByBit[absoluteBit] = splitSegment.colorName.trimmed();
                    splitDisplayFormatByBit[absoluteBit] = normalizedDisplayFormat(splitSegment.displayFormat);
                }
            }
        }

        visibleColumns_.reserve(totalBitCount);
        for (int absoluteBit = 0; absoluteBit < totalBitCount; ++absoluteBit) {
            features::columns::VisibleByteColumn visibleColumn;
            visibleColumn.byteIndex = absoluteBit / 8;
            visibleColumn.byteEndIndex = visibleColumn.byteIndex;
            visibleColumn.bitStart = absoluteBit % 8;
            visibleColumn.bitEnd = visibleColumn.bitStart;
            visibleColumn.absoluteStartBit = absoluteBit;
            visibleColumn.absoluteEndBit = absoluteBit;
            visibleColumn.definitionIndex = definitionIndexByBit.at(absoluteBit);
            visibleColumn.splitLabel = splitLabelByBit.at(absoluteBit);
            visibleColumn.splitColorName = splitColorByBit.at(absoluteBit).isEmpty()
                ? QStringLiteral("None")
                : splitColorByBit.at(absoluteBit);
            visibleColumn.splitDisplayFormat = splitDisplayFormatByBit.at(absoluteBit).isEmpty()
                ? QStringLiteral("binary")
                : splitDisplayFormatByBit.at(absoluteBit);
            visibleColumns_.append(visibleColumn);
        }

        rebuildByteColumnIndexMap();
        if (constantColumnHighlightEnabled_) {
            scheduleConstantVisibleColumnCacheRebuild();
        }
        return;
    }

    QVector<DefinitionSpanInfo> definitionSpans;
    QVector<QVector<int>> definitionsStartingByByte(widthBytes);
    QVector<QVector<QPair<int, int>>> definitionRangesByByte(widthBytes);
    if (!effectiveColumnDefinitions_.isEmpty()) {
        definitionSpans.reserve(effectiveColumnDefinitions_.size());
        for (int definitionIndex = 0; definitionIndex < effectiveColumnDefinitions_.size(); ++definitionIndex) {
            const features::columns::ByteColumnDefinition& definition = effectiveColumnDefinitions_.at(definitionIndex);
            const std::optional<QPair<int, int>> bitSpan = definitionBitSpan(definition);
            if (!bitSpan.has_value()) {
                continue;
            }

            DefinitionSpanInfo definitionSpan;
            definitionSpan.definitionIndex = definitionIndex;
            definitionSpan.startBit = bitSpan->first;
            definitionSpan.endBit = bitSpan->second;
            definitionSpan.startByte = bitSpan->first / 8;
            definitionSpan.endByte = bitSpan->second / 8;
            definitionSpan.visibleColumn = buildVisibleDefinitionColumn(definition, definitionIndex);
            definitionSpans.append(definitionSpan);

            if (definitionSpan.startByte >= 0 && definitionSpan.startByte < widthBytes) {
                definitionsStartingByByte[definitionSpan.startByte].append(definitionSpans.size() - 1);
            }

            for (int byteIndex = qMax(0, definitionSpan.startByte); byteIndex <= qMin(widthBytes - 1, definitionSpan.endByte); ++byteIndex) {
                const int byteStartBit = byteIndex * 8;
                definitionRangesByByte[byteIndex].append(
                    qMakePair(
                        qMax(0, definitionSpan.startBit - byteStartBit),
                        qMin(7, definitionSpan.endBit - byteStartBit)
                    )
                );
            }
        }
    }

    QVector<QVector<QPair<int, int>>> undefinedRangesByByte(widthBytes);
    for (int byteIndex = 0; byteIndex < widthBytes; ++byteIndex) {
        QVector<QPair<int, int>>& definitionRanges = definitionRangesByByte[byteIndex];
        if (definitionRanges.isEmpty()) {
            continue;
        }

        std::sort(definitionRanges.begin(), definitionRanges.end(), [](const auto& leftRange, const auto& rightRange) {
            return leftRange.first < rightRange.first;
        });

        QVector<QPair<int, int>>& undefinedRanges = undefinedRangesByByte[byteIndex];
        int currentBit = 0;
        for (const QPair<int, int>& definitionRange : definitionRanges) {
            if (currentBit < definitionRange.first) {
                undefinedRanges.append(qMakePair(currentBit, definitionRange.first - 1));
            }
            currentBit = qMax(currentBit, definitionRange.second + 1);
        }
        if (currentBit <= 7) {
            undefinedRanges.append(qMakePair(currentBit, 7));
        }
    }

    for (int byteIndex = 0; byteIndex < widthBytes; ++byteIndex) {
        const bool byteCoveredByDefinition = !definitionRangesByByte[byteIndex].isEmpty();
        QVector<features::columns::VisibleByteColumn> byteVisibleColumns;
        const QVector<int>& definitionIndexes = definitionsStartingByByte[byteIndex];
        for (int definitionSpanIndex : definitionIndexes) {
            if (definitionSpanIndex < 0 || definitionSpanIndex >= definitionSpans.size()) {
                continue;
            }
            byteVisibleColumns.append(definitionSpans.at(definitionSpanIndex).visibleColumn);
        }

        const QVector<QPair<int, int>> baseRanges = definitionRangesByByte[byteIndex].isEmpty()
            ? QVector<QPair<int, int>>{qMakePair(0, 7)}
            : undefinedRangesByByte[byteIndex];

        const QVector<SplitSegment> splitSegments = splitSegmentsForByte(byteIndex);
        for (const QPair<int, int>& baseRange : baseRanges) {
            bool appendedSplitColumn = false;
            for (const SplitSegment& splitSegment : splitSegments) {
                const int segmentStartBit = qMax(baseRange.first, splitSegment.startBit);
                const int segmentEndBit = qMin(baseRange.second, splitSegment.endBit);
                if (segmentStartBit > segmentEndBit) {
                    continue;
                }

                appendedSplitColumn = true;
                if (normalizedDisplayFormat(splitSegment.displayFormat) == QStringLiteral("binary")) {
                    for (int bitIndex = segmentStartBit; bitIndex <= segmentEndBit; ++bitIndex) {
                        features::columns::VisibleByteColumn visibleColumn;
                        visibleColumn.byteIndex = byteIndex;
                        visibleColumn.byteEndIndex = byteIndex;
                        visibleColumn.bitStart = bitIndex;
                        visibleColumn.bitEnd = bitIndex;
                        visibleColumn.absoluteStartBit = byteIndex * 8 + bitIndex;
                        visibleColumn.absoluteEndBit = byteIndex * 8 + bitIndex;
                        visibleColumn.splitLabel = splitSegment.label;
                        visibleColumn.splitColorName = splitSegment.colorName;
                        visibleColumn.splitDisplayFormat = QStringLiteral("binary");
                        byteVisibleColumns.append(visibleColumn);
                    }
                    continue;
                }

                features::columns::VisibleByteColumn visibleColumn;
                visibleColumn.byteIndex = byteIndex;
                visibleColumn.byteEndIndex = byteIndex;
                visibleColumn.bitStart = segmentStartBit;
                visibleColumn.bitEnd = segmentEndBit;
                visibleColumn.absoluteStartBit = byteIndex * 8 + segmentStartBit;
                visibleColumn.absoluteEndBit = byteIndex * 8 + segmentEndBit;
                visibleColumn.splitLabel = splitSegment.label;
                visibleColumn.splitColorName = splitSegment.colorName;
                visibleColumn.splitDisplayFormat = QStringLiteral("hex");
                byteVisibleColumns.append(visibleColumn);
            }

            if (!appendedSplitColumn) {
                features::columns::VisibleByteColumn visibleColumn;
                visibleColumn.byteIndex = byteIndex;
                visibleColumn.byteEndIndex = byteIndex;
                visibleColumn.bitStart = baseRange.first;
                visibleColumn.bitEnd = baseRange.second;
                visibleColumn.absoluteStartBit = byteIndex * 8 + baseRange.first;
                visibleColumn.absoluteEndBit = byteIndex * 8 + baseRange.second;
                visibleColumn.isUndefined = byteCoveredByDefinition;
                byteVisibleColumns.append(visibleColumn);
            }
        }

        std::sort(byteVisibleColumns.begin(), byteVisibleColumns.end(), [](const auto& leftColumn, const auto& rightColumn) {
            if (leftColumn.absoluteStartBit != rightColumn.absoluteStartBit) {
                return leftColumn.absoluteStartBit < rightColumn.absoluteStartBit;
            }
            return leftColumn.absoluteEndBit < rightColumn.absoluteEndBit;
        });
        visibleColumns_ += byteVisibleColumns;
    }

    rebuildByteColumnIndexMap();
    if (constantColumnHighlightEnabled_) {
        scheduleConstantVisibleColumnCacheRebuild();
    }
}

void ByteTableModel::rebuildByteColumnIndexMap() {
    const int widthBytes = displayWidthBytes();
    if (widthBytes <= 0) {
        byteColumnStarts_.clear();
        byteColumnCounts_.clear();
        return;
    }

    byteColumnStarts_.fill(-1, widthBytes);
    byteColumnCounts_.fill(0, widthBytes);

    for (int visibleColumnIndex = 0; visibleColumnIndex < visibleColumns_.size(); ++visibleColumnIndex) {
        const features::columns::VisibleByteColumn& visibleColumn = visibleColumns_.at(visibleColumnIndex);
        const int startByteIndex = qBound(0, visibleColumn.byteIndex, widthBytes - 1);
        const int endByteIndex = qBound(0, visibleColumn.byteEndIndex, widthBytes - 1);
        for (int byteIndex = startByteIndex; byteIndex <= endByteIndex; ++byteIndex) {
            if (byteColumnStarts_.at(byteIndex) < 0) {
                byteColumnStarts_[byteIndex] = visibleColumnIndex;
            }
            byteColumnCounts_[byteIndex] += 1;
        }
    }
}

qsizetype ByteTableModel::cellStartBit(int row, int col) const {
    const int visibleColumnIndex = visibleColumnIndexForModelColumn(col);
    if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
        return -1;
    }

    return rowStartBit(row) + visibleColumns_.at(visibleColumnIndex).absoluteStartBit;
}

qsizetype ByteTableModel::rowStartBit(int row) const {
    if (dataSource_ == nullptr || frameLayout_ == nullptr) {
        return -1;
    }
    return frameLayout_->rowStartBit(*dataSource_, row);
}

qsizetype ByteTableModel::rowLengthBits(int row) const {
    if (dataSource_ == nullptr || frameLayout_ == nullptr) {
        return 0;
    }
    return frameLayout_->rowLengthBits(*dataSource_, row);
}

QByteArray ByteTableModel::rowBitsSlice(int row, int startRelativeBit, int bitCount) const {
    QByteArray bitValues;
    if (dataSource_ == nullptr || frameLayout_ == nullptr || bitCount <= 0) {
        return bitValues;
    }

    const qsizetype rowBitLength = rowLengthBits(row);
    if (startRelativeBit < 0 || startRelativeBit >= rowBitLength) {
        return bitValues;
    }

    const qsizetype availableBitCount = qMin<qsizetype>(bitCount, rowBitLength - startRelativeBit);
    const qsizetype absoluteStartBit = rowStartBit(row) + startRelativeBit;
    return dataSource_->bitRange(absoluteStartBit, availableBitCount);
}

QByteArray ByteTableModel::rowBitsForVisibleColumn(
    int row,
    const features::columns::VisibleByteColumn& visibleColumn
) const {
    return rowBitsSlice(row, visibleColumn.absoluteStartBit, visibleColumn.bitWidth());
}

QByteArray ByteTableModel::rowBytesSlice(int row, int startByte, int byteCount) const {
    QByteArray rowBytes;
    if (dataSource_ == nullptr || frameLayout_ == nullptr || byteCount <= 0) {
        return rowBytes;
    }

    const qsizetype rowBitLength = rowLengthBits(row);
    const qsizetype startRelativeBit = static_cast<qsizetype>(startByte) * 8;
    if (startRelativeBit < 0 || startRelativeBit >= rowBitLength) {
        return rowBytes;
    }

    const qsizetype availableBitCount = qMin<qsizetype>(static_cast<qsizetype>(byteCount) * 8, rowBitLength - startRelativeBit);
    const int fullByteCount = static_cast<int>(availableBitCount / 8);
    if (fullByteCount <= 0) {
        return rowBytes;
    }

    const qsizetype absoluteStartBit = rowStartBit(row) + startRelativeBit;
    return dataSource_->byteRangeAtBitOffset(absoluteStartBit, fullByteCount);
}

QString ByteTableModel::formatRowHeaderText(int row) const {
    if (frameLayout_ == nullptr || dataSource_ == nullptr) {
        return {};
    }

    if (frameLayout_->isFramed()) {
        return QString::number(row);
    }

    return formatBitStartLabel(frameLayout_->rowStartBit(*dataSource_, row));
}

QString ByteTableModel::formatBytes(const QByteArray& rowBytes, const QString& displayFormat) const {
    if (rowBytes.isEmpty()) {
        return {};
    }

    const QString normalizedFormat = normalizedDisplayFormat(displayFormat);
    if (normalizedFormat == QStringLiteral("binary")) {
        QString binaryText;
        binaryText.reserve(rowBytes.size() * 8);
        for (unsigned char byteValue : rowBytes) {
            for (int bitShift = 7; bitShift >= 0; --bitShift) {
                binaryText.append(((byteValue >> bitShift) & 0x01) != 0 ? QLatin1Char('1') : QLatin1Char('0'));
            }
        }
        return binaryText;
    }

    if (normalizedFormat == QStringLiteral("decimal")) {
        quint64 numericValue = 0;
        for (unsigned char byteValue : rowBytes) {
            numericValue = (numericValue << 8) | byteValue;
        }
        return QString::number(numericValue);
    }

    if (normalizedFormat == QStringLiteral("ascii")) {
        QString asciiText;
        asciiText.reserve(rowBytes.size());
        for (unsigned char byteValue : rowBytes) {
            asciiText.append(core::formatPrintableAscii(byteValue));
        }
        return asciiText;
    }

    QString hexText = QStringLiteral("0x");
    for (unsigned char byteValue : rowBytes) {
        hexText.append(core::formatByteValue(byteValue));
    }
    return hexText;
}

QString ByteTableModel::formatBits(const QByteArray& rowBits, const QString& displayFormat) const {
    if (rowBits.isEmpty()) {
        return {};
    }

    const QString normalizedFormat = normalizedDisplayFormat(displayFormat);
    if (normalizedFormat == QStringLiteral("binary")) {
        QString binaryText;
        binaryText.reserve(rowBits.size());
        for (char bitValue : rowBits) {
            binaryText.append(bitValue != 0 ? QLatin1Char('1') : QLatin1Char('0'));
        }
        return binaryText;
    }

    quint64 numericValue = 0;
    for (char bitValue : rowBits) {
        numericValue = (numericValue << 1) | (bitValue != 0 ? 1 : 0);
    }

    if (normalizedFormat == QStringLiteral("decimal")) {
        return QString::number(numericValue);
    }

    return core::formatBitsAsHexWithTrailingBits(rowBits);
}

int ByteTableModel::displayWidthBytes() const {
    if (dataSource_ == nullptr || frameLayout_ == nullptr) {
        return 0;
    }
    return frameLayout_->columnCount(*dataSource_);
}

std::optional<ByteTableModel::SingleByteSelection> ByteTableModel::singleByteSelection(
    const QSet<int>& visibleColumns
) const {
    if (visibleColumns.isEmpty()) {
        return std::nullopt;
    }

    QList<int> sortedColumns = visibleColumns.values();
    std::sort(sortedColumns.begin(), sortedColumns.end());

    SingleByteSelection selection;
    bool firstColumn = true;
    for (int visibleColumnIndex : sortedColumns) {
        if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
            return std::nullopt;
        }

        const features::columns::VisibleByteColumn visibleColumn = visibleColumns_.at(visibleColumnIndex);
        if (visibleColumn.definitionIndex >= 0 || visibleColumn.isUndefined) {
            return std::nullopt;
        }

        if (firstColumn) {
            selection.byteIndex = visibleColumn.byteIndex;
            selection.startBit = visibleColumn.bitStart;
            selection.endBit = visibleColumn.bitEnd;
            firstColumn = false;
            continue;
        }

        if (visibleColumn.byteIndex != selection.byteIndex || visibleColumn.bitStart != selection.endBit + 1) {
            return std::nullopt;
        }
        selection.endBit = visibleColumn.bitEnd;
    }

    selection.bitWidth = selection.endBit - selection.startBit + 1;
    return selection;
}

QString ByteTableModel::splitGroupLabel(const QVector<SplitSegment>& splitSegments) const {
    for (const SplitSegment& splitSegment : splitSegments) {
        if (!splitSegment.label.trimmed().isEmpty() && !isAutoGeneratedLabel(splitSegment.label)) {
            return splitSegment.label.trimmed();
        }
    }
    return splitSegments.isEmpty() ? QString() : splitSegments.first().label.trimmed();
}

QString ByteTableModel::splitGroupColorName(const QVector<SplitSegment>& splitSegments) const {
    for (const SplitSegment& splitSegment : splitSegments) {
        if (!splitSegment.colorName.trimmed().isEmpty() && splitSegment.colorName != QStringLiteral("None")) {
            return splitSegment.colorName.trimmed();
        }
    }
    return QStringLiteral("None");
}

QVector<SplitSegment> ByteTableModel::currentSplitSegments(int byteIndex) const {
    return splitSegmentsForByte(byteIndex);
}

QVector<SplitSegment> ByteTableModel::replacementSegments(
    const QVector<SplitSegment>& sourceSegments,
    int targetStartBit,
    int targetEndBit,
    const QVector<SplitSegment>& replacementSegments
) const {
    QVector<SplitSegment> updatedSegments;
    bool replacedTarget = false;
    for (const SplitSegment& splitSegment : sourceSegments) {
        if (!replacedTarget
            && splitSegment.startBit == targetStartBit
            && splitSegment.endBit == targetEndBit) {
            updatedSegments += replacementSegments;
            replacedTarget = true;
            continue;
        }
        updatedSegments.append(splitSegment);
    }
    return replacedTarget ? updatedSegments : sourceSegments;
}

void ByteTableModel::applyCustomSplitSegments(
    int byteIndex,
    const SplitColumnState& splitState,
    const QVector<SplitSegment>& segments,
    int nextBinaryLabel,
    int nextNibbleLabel
) {
    SplitColumnState updatedState = splitState;
    updatedState.type = QStringLiteral("custom");
    updatedState.segments = segments;
    updatedState.nextBinaryLabel = nextBinaryLabel;
    updatedState.nextNibbleLabel = nextNibbleLabel;
    splitColumns_.insert(byteIndex, updatedState);
}

