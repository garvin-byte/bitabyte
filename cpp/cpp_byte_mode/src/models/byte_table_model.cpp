QString formatBitStartLabel(qsizetype bitOffset) {
    if (bitOffset < 0) {
        return {};
    }

    const qsizetype byteOffset = bitOffset / 8;
    const int bitOffsetInByte = static_cast<int>(bitOffset % 8);
    if (bitOffsetInByte == 0) {
        return core::formatByteOffset(byteOffset);
    }

    return QStringLiteral("%1+%2b").arg(core::formatByteOffset(byteOffset)).arg(bitOffsetInByte);
}

QString normalizedDisplayFormat(const QString& displayFormat) {
    const QString loweredFormat = displayFormat.trimmed().toLower();
    if (loweredFormat == QStringLiteral("dec")) {
        return QStringLiteral("decimal");
    }
    return loweredFormat.isEmpty() ? QStringLiteral("hex") : loweredFormat;
}

bool isAutoGeneratedLabel(const QString& labelText) {
    const QString trimmedLabel = labelText.trimmed();
    return trimmedLabel.isEmpty()
        || trimmedLabel.startsWith(QStringLiteral("BIN "))
        || trimmedLabel.startsWith(QStringLiteral("NIBBLE "))
        || trimmedLabel.startsWith(QStringLiteral("Byte "))
        || trimmedLabel.startsWith(QStringLiteral("Bytes "))
        || trimmedLabel.startsWith(QStringLiteral("Combo "));
}

QColor blendHighlightColor(const QColor& baseColor, const QColor& highlightColor, double highlightWeight) {
    if (!baseColor.isValid()) {
        return highlightColor;
    }

    const double clampedWeight = qBound(0.0, highlightWeight, 1.0);
    const double baseWeight = 1.0 - clampedWeight;
    return QColor(
        qRound((baseColor.red() * baseWeight) + (highlightColor.red() * clampedWeight)),
        qRound((baseColor.green() * baseWeight) + (highlightColor.green() * clampedWeight)),
        qRound((baseColor.blue() * baseWeight) + (highlightColor.blue() * clampedWeight))
    );
}

bool hasSnapshotDisplayValue(
    qsizetype rowLengthBits,
    const features::columns::VisibleByteColumn& visibleColumn
) {
    return visibleColumn.absoluteEndBit < rowLengthBits;
}

QByteArray rowBitsForSnapshotVisibleColumn(
    const data::ByteDataSource& dataSource,
    qsizetype rowStartBit,
    qsizetype rowLengthBits,
    const features::columns::VisibleByteColumn& visibleColumn
) {
    if (rowStartBit < 0 || !hasSnapshotDisplayValue(rowLengthBits, visibleColumn)) {
        return {};
    }

    const qsizetype absoluteStartBit = rowStartBit + visibleColumn.absoluteStartBit;
    return dataSource.bitRange(absoluteStartBit, visibleColumn.bitWidth());
}

const QColor kConstantHighlightColor(255, 247, 196);
const QColor kCounterHighlightColor(212, 232, 255);
const QColor kPatternHighlightColor(220, 245, 255);

bool rangesOverlap(
    qsizetype startBit,
    qsizetype endBit,
    const QVector<QPair<qsizetype, qsizetype>>& ranges
) {
    if (ranges.isEmpty() || endBit < startBit) {
        return false;
    }

    const auto candidate = std::lower_bound(
        ranges.begin(),
        ranges.end(),
        startBit,
        [](const QPair<qsizetype, qsizetype>& range, qsizetype value) {
            return range.second < value;
        }
    );
    return candidate != ranges.end() && candidate->first <= endBit;
}

bool byteColumnDefinitionsEqual(
    const QVector<features::columns::ByteColumnDefinition>& leftDefinitions,
    const QVector<features::columns::ByteColumnDefinition>& rightDefinitions
) {
    if (leftDefinitions.size() != rightDefinitions.size()) {
        return false;
    }

    for (int definitionIndex = 0; definitionIndex < leftDefinitions.size(); ++definitionIndex) {
        const features::columns::ByteColumnDefinition& leftDefinition = leftDefinitions.at(definitionIndex);
        const features::columns::ByteColumnDefinition& rightDefinition = rightDefinitions.at(definitionIndex);
        if (leftDefinition.label != rightDefinition.label
            || leftDefinition.startByte != rightDefinition.startByte
            || leftDefinition.endByte != rightDefinition.endByte
            || leftDefinition.unit != rightDefinition.unit
            || leftDefinition.startBit != rightDefinition.startBit
            || leftDefinition.totalBits != rightDefinition.totalBits
            || leftDefinition.displayFormat != rightDefinition.displayFormat
            || leftDefinition.colorName != rightDefinition.colorName) {
            return false;
        }
    }

    return true;
}

QVector<int> constantVisibleColumnIndicesForSnapshot(
    const data::ByteDataSource& dataSource,
    const features::framing::FrameLayout& frameLayout,
    const QVector<features::columns::VisibleByteColumn>& visibleColumns
) {
    QVector<int> constantVisibleColumnIndices;
    if (!dataSource.hasData() || visibleColumns.isEmpty()) {
        return constantVisibleColumnIndices;
    }

    const int totalRowCount = static_cast<int>(frameLayout.rowCount(dataSource));
    if (totalRowCount <= 0) {
        return constantVisibleColumnIndices;
    }

    struct RowGeometry {
        qsizetype startBit;
        qsizetype lengthBits;
    };
    QVector<RowGeometry> rowGeometries;
    rowGeometries.reserve(totalRowCount);
    for (int row = 0; row < totalRowCount; ++row) {
        rowGeometries.append({
            frameLayout.rowStartBit(dataSource, row),
            frameLayout.rowLengthBits(dataSource, row),
        });
    }

    constantVisibleColumnIndices.reserve(visibleColumns.size());
    for (int visibleColumnIndex = 0; visibleColumnIndex < visibleColumns.size(); ++visibleColumnIndex) {
        const features::columns::VisibleByteColumn& visibleColumn = visibleColumns.at(visibleColumnIndex);
        if (visibleColumn.isUndefined) {
            continue;
        }

        QByteArray referenceBits;
        bool hasReferenceValue = false;
        bool allRowsMatch = true;
        int analyzedRowCount = 0;

        for (int row = 0; row < totalRowCount; ++row) {
            const QByteArray rowBits = rowBitsForSnapshotVisibleColumn(
                dataSource,
                rowGeometries.at(row).startBit,
                rowGeometries.at(row).lengthBits,
                visibleColumn
            );
            if (rowBits.isEmpty()) {
                continue;
            }

            ++analyzedRowCount;
            if (!hasReferenceValue) {
                referenceBits = rowBits;
                hasReferenceValue = true;
                continue;
            }

            if (rowBits != referenceBits) {
                allRowsMatch = false;
                break;
            }
        }

        if (allRowsMatch && analyzedRowCount == totalRowCount && analyzedRowCount > 0) {
            constantVisibleColumnIndices.append(visibleColumnIndex);
        }
    }

    return constantVisibleColumnIndices;
}

