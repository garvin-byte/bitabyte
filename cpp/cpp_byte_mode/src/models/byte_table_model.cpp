QVector<SplitSegment> ByteTableModel::splitSegmentsForByte(int byteIndex) const {
    if (!splitColumns_.contains(byteIndex)) {
        return {};
    }

    const SplitColumnState splitState = splitColumns_.value(byteIndex);
    if (splitState.type == QStringLiteral("custom")) {
        return splitState.segments;
    }

    QVector<SplitSegment> splitSegments;
    if (splitState.type == QStringLiteral("binary")) {
        for (int bitIndex = 0; bitIndex < 8; ++bitIndex) {
            SplitSegment splitSegment;
            splitSegment.startBit = bitIndex;
            splitSegment.endBit = bitIndex;
            splitSegment.displayFormat = QStringLiteral("binary");
            splitSegment.label = splitState.label;
            splitSegment.colorName = splitState.colorName;
            splitSegments.append(splitSegment);
        }
        return splitSegments;
    }

    if (splitState.type == QStringLiteral("nibble")) {
        SplitSegment upperNibble;
        upperNibble.startBit = 0;
        upperNibble.endBit = 3;
        upperNibble.displayFormat = QStringLiteral("hex");
        upperNibble.label = splitState.label;
        upperNibble.colorName = splitState.colorName;
        splitSegments.append(upperNibble);

        SplitSegment lowerNibble;
        lowerNibble.startBit = 4;
        lowerNibble.endBit = 7;
        lowerNibble.displayFormat = QStringLiteral("hex");
        lowerNibble.label = splitState.label;
        lowerNibble.colorName = splitState.colorName;
        splitSegments.append(lowerNibble);
        return splitSegments;
    }

    return splitState.segments;
}

int ByteTableModel::nextSplitLabelIndex(const QString& prefix) const {
    int nextIndex = 0;
    for (auto splitIterator = splitColumns_.cbegin(); splitIterator != splitColumns_.cend(); ++splitIterator) {
        const SplitColumnState& splitState = splitIterator.value();
        QStringList candidateLabels;
        candidateLabels.append(splitState.label);
        for (const SplitSegment& splitSegment : splitState.segments) {
            candidateLabels.append(splitSegment.label);
        }

        for (const QString& candidateLabel : candidateLabels) {
            const QString trimmedLabel = candidateLabel.trimmed();
            if (!trimmedLabel.startsWith(prefix + QLatin1Char(' '))) {
                continue;
            }

            bool parsed = false;
            const int labelIndex = trimmedLabel.mid(prefix.size() + 1).toInt(&parsed);
            if (parsed) {
                nextIndex = qMax(nextIndex, labelIndex + 1);
            }
        }
    }

    return nextIndex;
}

QString ByteTableModel::nextAutoSplitColorName(
    const QString& preferredColor,
    const QSet<QString>& excludedColors
) const {
    static const std::array<QString, 6> autoColorSequence = {
        QStringLiteral("Sky"),
        QStringLiteral("Coral"),
        QStringLiteral("Mint"),
        QStringLiteral("Gold"),
        QStringLiteral("Lilac"),
        QStringLiteral("Sunshine"),
    };

    std::array<int, autoColorSequence.size()> usedCounts{};
    const auto colorIndexForName = [](QStringView colorName) {
        for (size_t colorIndex = 0; colorIndex < autoColorSequence.size(); ++colorIndex) {
            if (autoColorSequence[colorIndex] == colorName) {
                return static_cast<int>(colorIndex);
            }
        }
        return -1;
    };

    if (columnDefinitions_ != nullptr) {
        for (const features::columns::ByteColumnDefinition& columnDefinition : *columnDefinitions_) {
            const int colorIndex = colorIndexForName(columnDefinition.colorName);
            if (colorIndex >= 0) {
                usedCounts[static_cast<size_t>(colorIndex)] += 1;
            }
        }
    }

    for (auto splitIterator = splitColumns_.cbegin(); splitIterator != splitColumns_.cend(); ++splitIterator) {
        const SplitColumnState& splitState = splitIterator.value();
        const int splitStateColorIndex = colorIndexForName(splitState.colorName);
        if (splitStateColorIndex >= 0) {
            usedCounts[static_cast<size_t>(splitStateColorIndex)] += 1;
        }
        for (const SplitSegment& splitSegment : splitState.segments) {
            const int splitSegmentColorIndex = colorIndexForName(splitSegment.colorName);
            if (splitSegmentColorIndex >= 0) {
                usedCounts[static_cast<size_t>(splitSegmentColorIndex)] += 1;
            }
        }
    }

    int preferredIndex = 0;
    const int matchedPreferredIndex = colorIndexForName(preferredColor);
    if (matchedPreferredIndex >= 0) {
        preferredIndex = matchedPreferredIndex;
    }

    int bestIndex = -1;
    int bestCount = std::numeric_limits<int>::max();
    int bestDistance = std::numeric_limits<int>::max();
    for (size_t colorIndex = 0; colorIndex < autoColorSequence.size(); ++colorIndex) {
        if (excludedColors.contains(autoColorSequence[colorIndex])) {
            continue;
        }

        const int usedCount = usedCounts[colorIndex];
        const int colorDistance = (static_cast<int>(colorIndex) - preferredIndex + static_cast<int>(autoColorSequence.size()))
            % static_cast<int>(autoColorSequence.size());
        if (usedCount < bestCount || (usedCount == bestCount && colorDistance < bestDistance)) {
            bestIndex = static_cast<int>(colorIndex);
            bestCount = usedCount;
            bestDistance = colorDistance;
        }
    }

    if (bestIndex < 0) {
        bestIndex = 0;
    }
    return autoColorSequence[static_cast<size_t>(bestIndex)];
}

QSet<int> ByteTableModel::plainByteSplitTargets(const QSet<int>& visibleColumns) const {
    QSet<int> byteTargets;
    for (int visibleColumnIndex : visibleColumns) {
        if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
            continue;
        }

        const features::columns::VisibleByteColumn visibleColumn = visibleColumns_.at(visibleColumnIndex);
        if (visibleColumn.definitionIndex >= 0
            || visibleColumn.isUndefined
            || !visibleColumn.splitLabel.isEmpty()
            || visibleColumn.bitStart != 0
            || visibleColumn.bitEnd != 7) {
            continue;
        }
        byteTargets.insert(visibleColumn.byteIndex);
    }
    return byteTargets;
}

std::optional<QPair<int, int>> ByteTableModel::definitionBitSpan(
    const features::columns::ByteColumnDefinition& definition
) const {
    const int widthBytes = displayWidthBytes();
    if (widthBytes <= 0) {
        return std::nullopt;
    }

    if (definition.unit == QStringLiteral("bit")) {
        const int totalBits = qMax(1, definition.totalBits);
        const int startBit = qMax(0, definition.startBit);
        const int endBit = qMin(definition.startBit + totalBits - 1, widthBytes * 8 - 1);
        if (endBit < startBit) {
            return std::nullopt;
        }
        return QPair<int, int>(startBit, endBit);
    }

    const int startByte = qMax(0, definition.startByte);
    const int endByte = qMin(definition.endByte, widthBytes - 1);
    if (endByte < startByte) {
        return std::nullopt;
    }
    return QPair<int, int>(startByte * 8, (endByte + 1) * 8 - 1);
}

features::columns::VisibleByteColumn ByteTableModel::buildVisibleDefinitionColumn(
    const features::columns::ByteColumnDefinition& definition,
    int definitionIndex
) const {
    const std::optional<QPair<int, int>> bitSpan = definitionBitSpan(definition);
    if (!bitSpan.has_value()) {
        return {};
    }

    features::columns::VisibleByteColumn visibleColumn;
    visibleColumn.byteIndex = bitSpan->first / 8;
    visibleColumn.bitStart = bitSpan->first % 8;
    visibleColumn.byteEndIndex = bitSpan->second / 8;
    visibleColumn.bitEnd = bitSpan->second % 8;
    visibleColumn.absoluteStartBit = bitSpan->first;
    visibleColumn.absoluteEndBit = bitSpan->second;
    visibleColumn.definitionIndex = definitionIndex;
    return visibleColumn;
}

