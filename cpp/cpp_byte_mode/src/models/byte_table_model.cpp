ByteTableModel::ByteTableModel(
    data::ByteDataSource* dataSource,
    features::framing::FrameLayout* frameLayout,
    const QVector<features::columns::ByteColumnDefinition>* columnDefinitions,
    QObject* parent
)
    : QAbstractTableModel(parent),
      dataSource_(dataSource),
      frameLayout_(frameLayout),
      columnDefinitions_(columnDefinitions),
      constantHighlightWatcher_(new QFutureWatcher<QVector<int>>(this)) {
    connect(constantHighlightWatcher_, &QFutureWatcher<QVector<int>>::finished, this, [this]() {
        if (constantHighlightWatcher_ == nullptr) {
            return;
        }

        if (activeConstantHighlightRequestId_ == constantHighlightRequestId_ && constantColumnHighlightEnabled_) {
            constantVisibleColumnIndices_.clear();
            const QVector<int> constantVisibleColumns = constantHighlightWatcher_->result();
            for (int visibleColumnIndex : constantVisibleColumns) {
                constantVisibleColumnIndices_.insert(visibleColumnIndex);
            }
            if (rowCount() > 0 && columnCount() > 0) {
                emit dataChanged(
                    index(0, 0),
                    index(rowCount() - 1, columnCount() - 1),
                    {Qt::BackgroundRole}
                );
            }
        }

        if (constantColumnHighlightEnabled_ && activeConstantHighlightRequestId_ != constantHighlightRequestId_) {
            startConstantVisibleColumnCacheRebuild();
        }
    });
    monospaceFont_.setFamilies({
        QStringLiteral("Consolas"),
        QStringLiteral("Courier New"),
        QStringLiteral("Monospace"),
    });
    monospaceFont_.setStyleHint(QFont::StyleHint::Monospace);
    monospaceFont_.setHintingPreference(QFont::HintingPreference::PreferFullHinting);
    rebuildVisibleColumns();
}

int ByteTableModel::rowCount(const QModelIndex& parent) const {
    if (parent.isValid() || dataSource_ == nullptr || frameLayout_ == nullptr) {
        return 0;
    }

    return static_cast<int>(frameLayout_->rowCount(*dataSource_));
}

int ByteTableModel::columnCount(const QModelIndex& parent) const {
    if (parent.isValid() || dataSource_ == nullptr || frameLayout_ == nullptr) {
        return 0;
    }

    return visibleColumnCount() + (hasFrameLengthColumn() ? 1 : 0);
}

QVariant ByteTableModel::data(const QModelIndex& modelIndex, int role) const {
    if (!modelIndex.isValid() || dataSource_ == nullptr || frameLayout_ == nullptr) {
        return {};
    }

    if (isFrameLengthColumn(modelIndex.column())) {
        if (role == Qt::DisplayRole) {
            return frameLayout_->rowLengthBytes(*dataSource_, modelIndex.row());
        }

        if (role == Qt::TextAlignmentRole) {
            return Qt::AlignCenter;
        }

        if (role == Qt::FontRole) {
            return monospaceFont_;
        }

        if (role == Qt::BackgroundRole) {
            return QBrush(QColor(232, 238, 246));
        }

        if (role == Qt::ToolTipRole) {
            const qsizetype frameLengthBits = frameLayout_->rowLengthBits(*dataSource_, modelIndex.row());
            const int frameLengthBytes = frameLayout_->rowLengthBytes(*dataSource_, modelIndex.row());
            return QStringLiteral("Frame length %1 bytes | %2 bits")
                .arg(frameLengthBytes)
                .arg(frameLengthBits);
        }

        return {};
    }

    const int visibleColumnIndex = visibleColumnIndexForModelIndex(modelIndex);
    if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
        return {};
    }

    const features::columns::VisibleByteColumn visibleColumn = visibleColumns_.at(visibleColumnIndex);
    const qsizetype startBit = displayStartBitForIndex(modelIndex);

    if (role == Qt::DisplayRole) {
        return formatVisibleColumnDisplayText(modelIndex.row(), visibleColumn);
    }

    if (role == Qt::TextAlignmentRole) {
        return Qt::AlignCenter;
    }

    if (role == Qt::FontRole) {
        return monospaceFont_;
    }

    if (role == Qt::BackgroundRole) {
        if (!hasDisplayValue(modelIndex.row(), visibleColumn)) {
            return QBrush(QColor(242, 242, 242));
        }
        QColor backgroundColor = backgroundForVisibleColumn(visibleColumnIndex, visibleColumn);
        const qsizetype cellStartBit = rowStartBit(modelIndex.row()) + visibleColumn.absoluteStartBit;
        const qsizetype cellEndBit = rowStartBit(modelIndex.row()) + visibleColumn.absoluteEndBit;
        if (rangesOverlap(cellStartBit, cellEndBit, patternHighlightRanges_)) {
            backgroundColor = blendHighlightColor(
                backgroundColor,
                kPatternHighlightColor,
                backgroundColor.isValid() ? 0.65 : 1.0
            );
        }
        if (backgroundColor.isValid()) {
            return QBrush(backgroundColor);
        }
        return {};
    }

    if (role == Qt::ToolTipRole) {
        return formatVisibleColumnToolTip(modelIndex.row(), startBit, visibleColumn);
    }

    return {};
}

QVariant ByteTableModel::headerData(int section, Qt::Orientation orientation, int role) const {
    if (dataSource_ == nullptr || frameLayout_ == nullptr) {
        return {};
    }

    if (role == Qt::DisplayRole) {
        if (orientation == Qt::Horizontal) {
            if (isFrameLengthColumn(section)) {
                return QStringLiteral("Len");
            }

            const int visibleColumnIndex = visibleColumnIndexForModelColumn(section);
            if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
                return {};
            }
            return bottomHeaderLabelForVisibleColumn(visibleColumnIndex);
        }

        return formatRowHeaderText(section);
    }

    if (role == Qt::TextAlignmentRole) {
        return Qt::AlignCenter;
    }

    if (role == Qt::FontRole) {
        return monospaceFont_;
    }

    return {};
}

