bool ByteTableModel::canSplitByte(int byteIndex) const {
    if (isBitDisplayMode()) {
        return false;
    }

    if (byteIndex < 0 || byteIndex >= displayWidthBytes()) {
        return false;
    }

    if (splitColumns_.contains(byteIndex)) {
        return false;
    }

    if (byteIndex >= byteColumnStarts_.size() || byteIndex >= byteColumnCounts_.size()) {
        return false;
    }

    const int startColumn = byteColumnStarts_.at(byteIndex);
    const int columnCountForByte = byteColumnCounts_.at(byteIndex);
    if (startColumn < 0 || columnCountForByte != 1) {
        return false;
    }

    const features::columns::VisibleByteColumn visibleColumn = visibleColumns_.at(startColumn);
    return !visibleColumn.isUndefined
        && visibleColumn.definitionIndex < 0
        && visibleColumn.splitLabel.isEmpty()
        && visibleColumn.bitStart == 0
        && visibleColumn.bitEnd == 7;
}

bool ByteTableModel::applySplit(const QSet<int>& byteIndices, const QString& splitType) {
    if (isBitDisplayMode()) {
        return false;
    }

    const QString normalizedSplitType = splitType.trimmed().toLower();
    if (byteIndices.isEmpty()
        || (normalizedSplitType != QStringLiteral("binary") && normalizedSplitType != QStringLiteral("nibble"))) {
        return false;
    }

    bool changed = false;
    const QString labelPrefix = normalizedSplitType == QStringLiteral("binary")
        ? QStringLiteral("BIN")
        : QStringLiteral("NIBBLE");
    QSet<QString> usedColors;

    beginResetModel();
    for (int byteIndex : byteIndices) {
        if (!canSplitByte(byteIndex)) {
            continue;
        }

        SplitColumnState splitState;
        splitState.type = normalizedSplitType;
        splitState.label = QStringLiteral("%1 %2").arg(labelPrefix).arg(nextSplitLabelIndex(labelPrefix));
        splitState.colorName = nextAutoSplitColorName(
            normalizedSplitType == QStringLiteral("binary") ? QStringLiteral("Sky") : QStringLiteral("Coral"),
            usedColors
        );
        usedColors.insert(splitState.colorName);
        splitColumns_.insert(byteIndex, splitState);
        changed = true;
    }

    if (changed) {
        rebuildVisibleColumns();
    }
    endResetModel();
    return changed;
}

bool ByteTableModel::clearSplits(const QSet<int>& byteIndices) {
    if (isBitDisplayMode()) {
        return false;
    }

    if (byteIndices.isEmpty()) {
        return false;
    }

    bool changed = false;
    beginResetModel();
    for (int byteIndex : byteIndices) {
        changed = splitColumns_.remove(byteIndex) > 0 || changed;
    }
    if (changed) {
        rebuildVisibleColumns();
    }
    endResetModel();
    return changed;
}

bool ByteTableModel::removeSplitBitRange(int startBit, int endBit) {
    if (isBitDisplayMode()) {
        return false;
    }

    if (startBit > endBit) {
        return false;
    }

    bool changed = false;
    beginResetModel();

    const int startByteIndex = qMax(0, startBit / 8);
    const int endByteIndex = qMax(startByteIndex, endBit / 8);
    for (int byteIndex = startByteIndex; byteIndex <= endByteIndex; ++byteIndex) {
        if (!splitColumns_.contains(byteIndex)) {
            continue;
        }

        const int localStartBit = qMax(0, startBit - (byteIndex * 8));
        const int localEndBit = qMin(7, endBit - (byteIndex * 8));
        if (localStartBit > localEndBit) {
            continue;
        }

        const SplitColumnState splitState = splitColumns_.value(byteIndex);
        const QVector<SplitSegment> sourceSegments = currentSplitSegments(byteIndex);
        QVector<SplitSegment> remainingSegments;
        bool byteChanged = false;

        for (const SplitSegment& splitSegment : sourceSegments) {
            if (splitSegment.endBit < localStartBit || splitSegment.startBit > localEndBit) {
                remainingSegments.append(splitSegment);
                continue;
            }

            byteChanged = true;

            if (splitSegment.startBit < localStartBit) {
                SplitSegment leftSegment = splitSegment;
                leftSegment.endBit = localStartBit - 1;
                remainingSegments.append(leftSegment);
            }

            if (splitSegment.endBit > localEndBit) {
                SplitSegment rightSegment = splitSegment;
                rightSegment.startBit = localEndBit + 1;
                remainingSegments.append(rightSegment);
            }
        }

        if (!byteChanged) {
            continue;
        }

        if (remainingSegments.isEmpty()) {
            splitColumns_.remove(byteIndex);
        } else {
            applyCustomSplitSegments(
                byteIndex,
                splitState,
                remainingSegments,
                splitState.nextBinaryLabel,
                splitState.nextNibbleLabel
            );
        }
        changed = true;
    }

    if (changed) {
        rebuildVisibleColumns();
    }
    endResetModel();
    return changed;
}

