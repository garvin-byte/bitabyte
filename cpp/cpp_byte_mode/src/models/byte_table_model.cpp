QSet<int> ByteTableModel::splitByteTargetsForVisibleColumns(const QSet<int>& visibleColumns) const {
    QSet<int> splitByteTargets;
    for (int visibleColumnIndex : visibleColumns) {
        if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
            continue;
        }
        const features::columns::VisibleByteColumn visibleColumn = visibleColumns_.at(visibleColumnIndex);
        if (!visibleColumn.splitLabel.isEmpty() || splitColumns_.contains(visibleColumn.byteIndex)) {
            splitByteTargets.insert(visibleColumn.byteIndex);
        }
    }
    return splitByteTargets;
}

bool ByteTableModel::selectionCanBecomeNibble(const QSet<int>& visibleColumns) const {
    if (isBitDisplayMode()) {
        return false;
    }

    const std::optional<SingleByteSelection> selection = singleByteSelection(visibleColumns);
    if (!selection.has_value() || selection->bitWidth != 4 || !splitColumns_.contains(selection->byteIndex)) {
        return false;
    }

    const QVector<SplitSegment> splitSegments = currentSplitSegments(selection->byteIndex);
    for (const SplitSegment& splitSegment : splitSegments) {
        if (normalizedDisplayFormat(splitSegment.displayFormat) != QStringLiteral("binary")) {
            continue;
        }
        if (splitSegment.startBit <= selection->startBit && selection->endBit <= splitSegment.endBit) {
            return true;
        }
    }

    return false;
}

bool ByteTableModel::selectionCanBecomeBinary(const QSet<int>& visibleColumns) const {
    if (isBitDisplayMode()) {
        return false;
    }

    const std::optional<SingleByteSelection> selection = singleByteSelection(visibleColumns);
    if (!selection.has_value() || selection->bitWidth != 4 || !splitColumns_.contains(selection->byteIndex)) {
        return false;
    }

    const QVector<SplitSegment> splitSegments = currentSplitSegments(selection->byteIndex);
    for (const SplitSegment& splitSegment : splitSegments) {
        if (normalizedDisplayFormat(splitSegment.displayFormat) != QStringLiteral("hex")) {
            continue;
        }
        if (splitSegment.startBit == selection->startBit && splitSegment.endBit == selection->endBit) {
            return true;
        }
    }

    return false;
}

bool ByteTableModel::convertSelectionToNibble(const QSet<int>& visibleColumns) {
    if (isBitDisplayMode()) {
        return false;
    }

    const std::optional<SingleByteSelection> selection = singleByteSelection(visibleColumns);
    if (!selection.has_value() || selection->bitWidth != 4) {
        return false;
    }

    const int byteIndex = selection->byteIndex;
    if (!splitColumns_.contains(byteIndex)) {
        return false;
    }

    const SplitColumnState splitState = splitColumns_.value(byteIndex);
    const QVector<SplitSegment> sourceSegments = currentSplitSegments(byteIndex);
    if (sourceSegments.isEmpty()) {
        return false;
    }

    SplitSegment targetSegment;
    bool foundTargetSegment = false;
    for (const SplitSegment& splitSegment : sourceSegments) {
        if (normalizedDisplayFormat(splitSegment.displayFormat) != QStringLiteral("binary")) {
            continue;
        }
        if (splitSegment.startBit <= selection->startBit && selection->endBit <= splitSegment.endBit) {
            targetSegment = splitSegment;
            foundTargetSegment = true;
            break;
        }
    }
    if (!foundTargetSegment) {
        return false;
    }

    int nextBinaryLabel = splitState.nextBinaryLabel;
    int nextNibbleLabel = splitState.nextNibbleLabel;
    QVector<SplitSegment> replacement;

    auto appendBinarySegment = [&](int startBit, int endBit) {
        if (startBit > endBit) {
            return;
        }
        SplitSegment splitSegment;
        splitSegment.startBit = startBit;
        splitSegment.endBit = endBit;
        splitSegment.displayFormat = QStringLiteral("binary");
        splitSegment.label = QStringLiteral("BIN %1").arg(nextBinaryLabel++);
        splitSegment.colorName = nextAutoSplitColorName(QStringLiteral("Sky"));
        replacement.append(splitSegment);
    };

    appendBinarySegment(targetSegment.startBit, selection->startBit - 1);

    SplitSegment nibbleSegment;
    nibbleSegment.startBit = selection->startBit;
    nibbleSegment.endBit = selection->endBit;
    nibbleSegment.displayFormat = QStringLiteral("hex");
    nibbleSegment.label = QStringLiteral("NIBBLE %1").arg(nextNibbleLabel++);
    nibbleSegment.colorName = nextAutoSplitColorName(QStringLiteral("Coral"));
    replacement.append(nibbleSegment);

    appendBinarySegment(selection->endBit + 1, targetSegment.endBit);

    beginResetModel();
    applyCustomSplitSegments(
        byteIndex,
        splitState,
        replacementSegments(sourceSegments, targetSegment.startBit, targetSegment.endBit, replacement),
        nextBinaryLabel,
        nextNibbleLabel
    );
    rebuildVisibleColumns();
    endResetModel();
    return true;
}

bool ByteTableModel::convertSelectionToBinary(const QSet<int>& visibleColumns) {
    if (isBitDisplayMode()) {
        return false;
    }

    const std::optional<SingleByteSelection> selection = singleByteSelection(visibleColumns);
    if (!selection.has_value() || selection->bitWidth != 4) {
        return false;
    }

    const int byteIndex = selection->byteIndex;
    if (!splitColumns_.contains(byteIndex)) {
        return false;
    }

    const SplitColumnState splitState = splitColumns_.value(byteIndex);
    const QVector<SplitSegment> sourceSegments = currentSplitSegments(byteIndex);
    if (sourceSegments.isEmpty()) {
        return false;
    }

    SplitSegment targetSegment;
    bool foundTargetSegment = false;
    for (const SplitSegment& splitSegment : sourceSegments) {
        if (normalizedDisplayFormat(splitSegment.displayFormat) != QStringLiteral("hex")) {
            continue;
        }
        if (splitSegment.startBit == selection->startBit && splitSegment.endBit == selection->endBit) {
            targetSegment = splitSegment;
            foundTargetSegment = true;
            break;
        }
    }
    if (!foundTargetSegment) {
        return false;
    }

    int nextBinaryLabel = splitState.nextBinaryLabel;
    QVector<SplitSegment> replacement;
    for (int bitIndex = selection->startBit; bitIndex <= selection->endBit; ++bitIndex) {
        SplitSegment splitSegment;
        splitSegment.startBit = bitIndex;
        splitSegment.endBit = bitIndex;
        splitSegment.displayFormat = QStringLiteral("binary");
        splitSegment.label = QStringLiteral("BIN %1").arg(nextBinaryLabel++);
        splitSegment.colorName = nextAutoSplitColorName(QStringLiteral("Sky"));
        replacement.append(splitSegment);
    }

    beginResetModel();
    applyCustomSplitSegments(
        byteIndex,
        splitState,
        replacementSegments(sourceSegments, targetSegment.startBit, targetSegment.endBit, replacement),
        nextBinaryLabel,
        splitState.nextNibbleLabel
    );
    rebuildVisibleColumns();
    endResetModel();
    return true;
}

bool ByteTableModel::visibleColumnsAreContiguous(const QSet<int>& visibleColumns) const {
    if (visibleColumns.isEmpty()) {
        return false;
    }

    QList<int> sortedColumns = visibleColumns.values();
    std::sort(sortedColumns.begin(), sortedColumns.end());

    int previousEndBit = -1;
    for (int visibleColumnIndex : sortedColumns) {
        if (visibleColumnIndex < 0 || visibleColumnIndex >= visibleColumns_.size()) {
            return false;
        }

        const features::columns::VisibleByteColumn visibleColumn = visibleColumns_.at(visibleColumnIndex);
        if (previousEndBit >= 0 && visibleColumn.absoluteStartBit != previousEndBit + 1) {
            return false;
        }
        previousEndBit = visibleColumn.absoluteEndBit;
    }

    return true;
}

