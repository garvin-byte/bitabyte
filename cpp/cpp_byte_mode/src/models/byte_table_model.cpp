QVector<SplitPanelEntry> ByteTableModel::visibleSplitEntries() const {
    QVector<SplitPanelEntry> splitEntries;
    QList<int> sortedByteIndices = splitColumns_.keys();
    std::sort(sortedByteIndices.begin(), sortedByteIndices.end());

    for (int byteIndex : sortedByteIndices) {
        const SplitColumnState splitState = splitColumns_.value(byteIndex);
        if (splitState.type == QStringLiteral("custom")) {
            QVector<SplitSegment> splitSegments = splitState.segments;
            std::sort(splitSegments.begin(), splitSegments.end(), [](const SplitSegment& leftSegment, const SplitSegment& rightSegment) {
                return leftSegment.startBit < rightSegment.startBit;
            });

            QVector<SplitSegment> mergedSegments;
            for (const SplitSegment& splitSegment : splitSegments) {
                if (!mergedSegments.isEmpty()) {
                    SplitSegment& mergedSegment = mergedSegments.last();
                    if (mergedSegment.endBit + 1 == splitSegment.startBit
                        && mergedSegment.label == splitSegment.label
                        && normalizedDisplayFormat(mergedSegment.displayFormat)
                            == normalizedDisplayFormat(splitSegment.displayFormat)
                        && mergedSegment.colorName == splitSegment.colorName) {
                        mergedSegment.endBit = splitSegment.endBit;
                        continue;
                    }
                }
                mergedSegments.append(splitSegment);
            }

            for (const SplitSegment& splitSegment : mergedSegments) {
                SplitPanelEntry splitEntry;
                splitEntry.payloadByteIndex = byteIndex;
                splitEntry.startBit = byteIndex * 8 + splitSegment.startBit;
                splitEntry.endBit = byteIndex * 8 + splitSegment.endBit;
                splitEntry.label = splitSegment.label;
                splitEntry.displayFormat = normalizedDisplayFormat(splitSegment.displayFormat);
                splitEntry.colorName = splitSegment.colorName;
                splitEntries.append(splitEntry);
            }
            continue;
        }

        const QVector<SplitSegment> splitSegments = splitSegmentsForByte(byteIndex);
        if (splitSegments.isEmpty()) {
            continue;
        }

        SplitPanelEntry splitEntry;
        splitEntry.payloadByteIndex = byteIndex;
        splitEntry.startBit = byteIndex * 8 + splitSegments.first().startBit;
        splitEntry.endBit = byteIndex * 8 + splitSegments.last().endBit;
        splitEntry.label = splitState.label;
        splitEntry.displayFormat = normalizedDisplayFormat(splitSegments.first().displayFormat);
        splitEntry.colorName = splitState.colorName;
        splitEntries.append(splitEntry);
    }

    return splitEntries;
}

QString ByteTableModel::topHeaderLabelForVisibleColumn(int visibleColumn) const {
    if (visibleColumn < 0 || visibleColumn >= visibleColumns_.size()) {
        return {};
    }

    const features::columns::VisibleByteColumn& visibleColumnEntry = visibleColumns_.at(visibleColumn);
    if (isBitDisplayMode()) {
        if (visibleColumnEntry.definitionIndex >= 0) {
            const features::columns::ByteColumnDefinition* columnDefinition =
                definitionAtIndex(visibleColumnEntry.definitionIndex);
            if (columnDefinition != nullptr) {
                const QString labelText = columnDefinition->label.trimmed();
                if (!labelText.isEmpty()) {
                    return labelText;
                }
            }
        }

        const QString splitLabel = visibleColumnEntry.splitLabel.trimmed();
        if (!splitLabel.isEmpty()) {
            return splitLabel;
        }

        return formatRangeLabel(visibleColumnEntry.byteIndex, visibleColumnEntry.byteEndIndex);
    }

    if (visibleColumnEntry.definitionIndex >= 0) {
        const features::columns::ByteColumnDefinition* columnDefinition = definitionAtIndex(visibleColumnEntry.definitionIndex);
        if (columnDefinition != nullptr) {
            return columnDefinition->label.trimmed();
        }
        return {};
    }

    return visibleColumnEntry.splitLabel.trimmed();
}

QString ByteTableModel::bottomHeaderLabelForVisibleColumn(int visibleColumn) const {
    if (visibleColumn < 0 || visibleColumn >= visibleColumns_.size()) {
        return {};
    }

    const features::columns::VisibleByteColumn& visibleColumnEntry = visibleColumns_.at(visibleColumn);
    if (isBitDisplayMode()) {
        return QString::number(visibleColumnEntry.bitStart);
    }
    return formatRangeLabel(visibleColumnEntry.byteIndex, visibleColumnEntry.byteEndIndex);
}

