    activeConstantHighlightRequestId_ = constantHighlightRequestId_;
    const data::ByteDataSource dataSourceSnapshot = *dataSource_;
    const features::framing::FrameLayout frameLayoutSnapshot = *frameLayout_;
    const QVector<features::columns::VisibleByteColumn> visibleColumnsSnapshot = visibleColumns_;
