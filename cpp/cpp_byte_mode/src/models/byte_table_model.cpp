QString ByteTableModel::formatVisibleColumnDisplayText(
    int row,
    const features::columns::VisibleByteColumn& visibleColumn
) const {
    if (!hasDisplayValue(row, visibleColumn)) {
        return {};
    }

    if (isBitDisplayMode()) {
        const QByteArray rowBits = rowBitsForVisibleColumn(row, visibleColumn);
        if (rowBits.isEmpty()) {
            return {};
        }
        return rowBits.at(0) != 0 ? QStringLiteral("1") : QStringLiteral("0");
    }

    if (visibleColumn.definitionIndex >= 0) {
        const features::columns::ByteColumnDefinition* columnDefinition = definitionAtIndex(visibleColumn.definitionIndex);
        if (columnDefinition == nullptr) {
            return {};
        }
        if (columnDefinition->unit == QStringLiteral("byte")) {
            return formatBytes(
                rowBytesSlice(row, columnDefinition->startByte, columnDefinition->byteCount()),
                columnDefinition->displayFormat
            );
        }
        return formatBits(
            rowBitsSlice(row, columnDefinition->startBit, qMax(1, columnDefinition->totalBits)),
            columnDefinition->displayFormat
        );
    }

    return formatBits(rowBitsForVisibleColumn(row, visibleColumn), visibleColumn.splitDisplayFormat);
}

QString ByteTableModel::formatVisibleColumnToolTip(
    int row,
    qsizetype startBit,
    const features::columns::VisibleByteColumn& visibleColumn
) const {
    if (!hasDisplayValue(row, visibleColumn)) {
        return QStringLiteral("No data in this frame position.");
    }

    QString toolTipText = QStringLiteral("Start bit %1").arg(startBit);
    if (visibleColumn.definitionIndex >= 0) {
        const features::columns::ByteColumnDefinition* columnDefinition = definitionAtIndex(visibleColumn.definitionIndex);
        if (columnDefinition != nullptr) {
            toolTipText += QStringLiteral(" | Column %1 | Format %2")
                .arg(columnDefinition->label.isEmpty() ? QStringLiteral("(unnamed)") : columnDefinition->label)
                .arg(columnDefinition->displayFormat);
        }
    } else if (!visibleColumn.splitLabel.isEmpty()) {
        toolTipText += QStringLiteral(" | Split %1").arg(visibleColumn.splitLabel);
    }
    toolTipText += QStringLiteral(" | Value %1").arg(formatVisibleColumnDisplayText(row, visibleColumn));
    return toolTipText;
}

QColor ByteTableModel::backgroundForVisibleColumn(
    int visibleColumnIndex,
    const features::columns::VisibleByteColumn& visibleColumn
) const {
    const features::columns::ByteColumnDefinition* columnDefinition =
        visibleColumn.definitionIndex >= 0 ? definitionAtIndex(visibleColumn.definitionIndex) : nullptr;
    QColor backgroundColor;
    if (columnDefinition != nullptr) {
        backgroundColor = features::columns::colorForName(columnDefinition->colorName);
    } else if (!visibleColumn.splitColorName.trimmed().isEmpty()
        && visibleColumn.splitColorName.trimmed() != QStringLiteral("None")) {
        backgroundColor = features::columns::colorForName(visibleColumn.splitColorName.trimmed());
    }

    if (constantColumnHighlightEnabled_
        && constantVisibleColumnIndices_.contains(visibleColumnIndex)) {
        return blendHighlightColor(backgroundColor, kConstantHighlightColor, backgroundColor.isValid() ? 0.62 : 1.0);
    }

    if (counterVisibleColumnIndices_.contains(visibleColumnIndex)) {
        return blendHighlightColor(backgroundColor, kCounterHighlightColor, backgroundColor.isValid() ? 0.62 : 1.0);
    }

    return backgroundColor;
}

